interface Snapshot {
  manifestHash: string;
  sourceFiles: number;
  buckets: Array<{ id: string; organs: number }>;
  voices: number;
}

export function snapshotFromAgents(text: string): Snapshot {
  const manifestHash = text.match(
    /source_manifest_hash: (sha256:[0-9a-f]+)/,
  )?.[1];
  const sourceFiles = Number(text.match(/source_files: (\d+)/)?.[1]);
  if (!manifestHash || !Number.isSafeInteger(sourceFiles)) {
    throw new Error("agents bootstrap lacks a valid source receipt");
  }

  const buckets = [...text.matchAll(
    /^- bucket ([0-9A-F]): (\d+) organs/mg,
  )].map((m) => ({ id: m[1], organs: Number(m[2]) }));
  if (buckets.length === 0) throw new Error("agents bootstrap has no topology");

  const voiceSection = text.split("## Voice Resolution")[1]?.split(
    "## Commands",
  )[0] ?? "";
  const voices =
    voiceSection.split("\n").filter((line) =>
      /^\| [^|-].*\|$/.test(line) && !line.startsWith("| voice ")
    ).length;
  if (voices === 0) throw new Error("agents bootstrap has no voice rows");
  return { manifestHash, sourceFiles, buckets, voices };
}

