export async function runReadmeGenerator(argv: string[]): Promise<void> {
  const snapshot = snapshotFromAgents(await Deno.readTextFile(AGENTS));
  await Deno.writeTextFile(OUT, renderReadme(snapshot) + "\n");
  await formatGeneratedFile(OUT);
  const receipt = {
    type: "readme",
    position: "8/85",
    action: "generate",
    stable: argv.includes("--stable"),
    source_manifest_hash: snapshot.manifestHash,
    written: "src/x88E0_readme_bootstrap.myc.md",
  };
  console.log(JSON.stringify(receipt));
}

if (import.meta.main) await runReadmeGenerator(Deno.args);
